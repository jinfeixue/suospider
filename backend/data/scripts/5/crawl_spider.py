def log(level, msg):
    ts = datetime.datetime.utcnow().strftime("%Y-%m-%d %H:%M:%S")
    print(f"[{ts}] [{level}] {msg}", flush=True)


def save_urls_to_db(urls):
    """Save crawled URLs to database."""
    if not urls:
        return 0

    conn = pymysql.connect(
        host=DB_HOST, port=DB_PORT, user=DB_USER, 
        password=DB_PASSWORD, database=DB_NAME,
        charset='utf8mb4', connect_timeout=10
    )
    cursor = conn.cursor()

    count = 0
    for url in urls:
        try:
            cursor.execute(f"SELECT id FROM `{DB_TABLE}` WHERE url = %s", (url,))
            if cursor.fetchone():
                continue

            cursor.execute(
                f"INSERT INTO `{DB_TABLE}` (url, parseflag) VALUES (%s, 0)",
                (url,)
            )
            count += 1
        except Exception as e:
            log("WARNING", f"Failed to insert URL: {e}")

    conn.commit()
    cursor.close()
    conn.close()
    return count


