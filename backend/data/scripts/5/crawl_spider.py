async def crawl():
    from app.engine.engine_factory import get_engine
    from app.engine import EngineConfig

    engine = get_engine(ENGINE_NAME)
    all_urls = []

    async with engine:

            # 页码递增模式
            PAGE_URL_TEMPLATE = "https://www.ceri.com.cn/xwzx/jcyw/index_{page_num}.html"
            PAGE_START = 1
            PAGE_STEP = 1

            for page_num_offset in range(MAX_PAGES):
                current_page_num = PAGE_START + page_num_offset * PAGE_STEP
                current_url = PAGE_URL_TEMPLATE.replace("{page_num}", str(current_page_num))
                log("INFO", f"Fetching page {page_num_offset + 1}: {current_url}")

                ec = EngineConfig(url=current_url, headers=HEADERS, timeout=TIMEOUT)
                resp = await engine.fetch(ec)

                if not resp.ok:
                    log("ERROR", f"Page {page_num_offset + 1} failed: {resp.error}")
                    break

                log("INFO", f"Page {page_num_offset + 1}: {resp.status_code} - {len(resp.text)} chars")

                try:
                    tree = lxml_html.fromstring(resp.content)
                except Exception as e:
                    log("ERROR", f"Failed to parse HTML: {e}")
                    break

                # Extract URLs
                urls = tree.xpath(LIST_XPATH)
                log("INFO", f"Page {page_num_offset + 1}: found {len(urls)} URLs")

                for u in urls:
                    if u and not u.startswith("http"):
                        u = urljoin(current_url, u)
                    if u and u.startswith("http"):
                        all_urls.append(u)

                time.sleep(INTERVAL)


    # Remove duplicates
    unique_urls = list(dict.fromkeys(all_urls))
    log("INFO", f"=== Crawl completed: {len(unique_urls)} unique URLs collected ===")

    # Save to database
    saved = save_urls_to_db(unique_urls)
    log("INFO", f"Saved {saved} new URLs to database")

    # Update task stats
    if saved > 0:
        update_task_stats(saved)
        log("INFO", f"Updated task stats: +{saved} crawled")

    return unique_urls


if __name__ == "__main__":
    asyncio.run(crawl())
